# Dictionary For Loop
THISDICT ={
      "name":"Al ahad",
      "father":"Nijam",
      "mother":"Raseena"
}
for a in THISDICT:
   print(a)

example = {'color' : 'red '  , 
           'fruit' : 'apple' ,
           'place' : 'United arab emirates '
}
for u in example:
  print(example[u])

example = {'color' : 'red '  , 
           'fruit' : 'apple' ,
           'place' : 'United arab emirates '
}
for x in example.values():
  print(x)

